package br.com.portal.model.dashboard;

public class ModalHistoricoContrato {
	
	private String id_contrato;
	private String historico_contrato;
	private String renovacao_contrato_novo;
	private String renovacao_contrato_origem;
	private String dt_inicio_vigencia;
	private String dt_final_vigencia;
	private String qty_meses_vigencia;
	
	public String getId_contrato() {
		return id_contrato;
	}
	public void setId_contrato(String id_contrato) {
		this.id_contrato = id_contrato;
	}
	public String getHistorico_contrato() {
		return historico_contrato;
	}
	public void setHistorico_contrato(String historico_contrato) {
		this.historico_contrato = historico_contrato;
	}
	public String getRenovacao_contrato_novo() {
		return renovacao_contrato_novo;
	}
	public void setRenovacao_contrato_novo(String renovacao_contrato_novo) {
		this.renovacao_contrato_novo = renovacao_contrato_novo;
	}
	public String getRenovacao_contrato_origem() {
		return renovacao_contrato_origem;
	}
	public void setRenovacao_contrato_origem(String renovacao_contrato_origem) {
		this.renovacao_contrato_origem = renovacao_contrato_origem;
	}
	public String getDt_inicio_vigencia() {
		return dt_inicio_vigencia;
	}
	public void setDt_inicio_vigencia(String dt_inicio_vigencia) {
		this.dt_inicio_vigencia = dt_inicio_vigencia;
	}
	public String getDt_final_vigencia() {
		return dt_final_vigencia;
	}
	public void setDt_final_vigencia(String dt_final_vigencia) {
		this.dt_final_vigencia = dt_final_vigencia;
	}
	public String getQty_meses_vigencia() {
		return qty_meses_vigencia;
	}
	public void setQty_meses_vigencia(String qty_meses_vigencia) {
		this.qty_meses_vigencia = qty_meses_vigencia;
	}

	

}
